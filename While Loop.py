# While loop is to allow us to loop through and execute a block of code multiple times
i = 1 
while i <= 10:  # i <= 10 is a condition when i >10, it will stop the loop, if it meets the condition, continue 
    print(i) # print i = 1, 
    i += 1 # after 1, execute i+=1 = new i, and then use the new i to go back to while loop and check the condition again until the condition falses

print("Done")
