friends = ["Eric", "Bonnie", "Zoe", "Ken"]  # [] is to make a list
print (friends)
print (friends[0]) # index
print (friends[-1]) # back index
print (friends[1:]) # 1: is to select element starting at 1
print (friends[1:3]) # select from 1 to 2, not 3
friends[3] = "summer" # change name
print (friends[3])